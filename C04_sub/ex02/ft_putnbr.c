#include <unistd.h>

void	ft_1(int nb)
{
	char	c;

	if (nb > 0)
	{
		ft_1(nb / 10);
		c = (nb % 10) + '0';
		write(1, &c, 1);
	}
}

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb *= -1;
	}
	if (nb == 0)
	{
		write(1, "0", 1);
		return ;
	}
	ft_1(nb);
}
