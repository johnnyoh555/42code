int	ft_is_prime(int nb)
{
	int	i;
	int	cnt;

	i = 1;
	cnt = 0;
	if (nb < 2)
		return (0);
	while (i * i <= nb && i * i > 0)
	{
		if (nb % i == 0)
			cnt++;
		if (cnt >= 2)
			break ;
		i++;
	}
	if (cnt == 1)
		return (1);
	return (0);
}
