#include <unistd.h>

void	ft_print_comb(void)
{
	char	arr[3];

	arr[0] = '0';
	while (arr[0] <= '9')
	{
		arr[1] = arr[0] + 1;
		while (arr[1] <= '9')
		{
			arr[2] = arr[1] + 1;
			while (arr[2] <= '9')
			{
				if (arr[0] < arr[1] && arr[1] < arr[2])
				{
					write (1, arr, 3);
					if (arr[0] == '7' && arr[1] == '8' && arr[2] == '9')
						break ;
					write (1, ", ", 2);
				}
				arr[2]++;
			}
			arr[1]++;
		}
		arr[0]++;
	}	
}
