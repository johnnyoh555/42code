char	*ft_strlowcase(char *str)
{
	char	*ret;

	ret = str;
	while (*str != 0)
	{
		if (*str >= 'A' && *str <= 'Z')
			*str = *str - ('A' - 'a');
		str++;
	}
	str = ret;
	return (str);
}
